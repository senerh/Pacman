        //METHODS
        private Vector2 PacmanPosition(byte[,] map)
        {
            for (int y = 0; y < Grid.GRID_HEIGHT; y++)
            {
                for (int x = 0; x < Grid.GRID_WIDTH; x++)
                {
                    if (map[y, x] == Grid.PACMAN)
                        return new Vector2(x, y);
                }
            }
            return new Vector2(0, 0);
        }

