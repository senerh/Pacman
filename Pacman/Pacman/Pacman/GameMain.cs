            {
                startNewLevel = false;
                grid.Draw(spriteBatch);
                pacman.Draw(spriteBatch);
                ghostRed.Draw(spriteBatch);
                Resources.beginningSound.Play();
            }
            else if (grid.isFinished())
            {
                loadNextLevel();
            }
            else
            {
                grid.Draw(spriteBatch);
                pacman.Draw(spriteBatch);
